import http from "node:http";

const port = Number.parseInt(process.env.PORT ?? "8080", 10);

function conceptFromPrompt(prompt) {
  const primaryColor = prompt.match(/"primaryColor":"(#[0-9a-fA-F]{6})"/)?.[1] ?? "#2563eb";
  const secondaryColor =
    prompt.match(/"secondaryColor":"(#[0-9a-fA-F]{6})"/)?.[1] ?? "#111827";
  const id = prompt.match(/Use id:\s*([a-z0-9-]+)/i)?.[1] ?? "local-mock-1";
  const identity = /photo-led|identity/i.test(prompt);
  const creative = /creative|decorative art/i.test(prompt);
  const premium = /premium|immersive/i.test(prompt);
  const mood = identity ? "identity" : creative ? "creative" : premium ? "premium" : "official";

  return {
    id,
    name: identity
      ? "Local Identity Pass"
      : creative
        ? "Local Creative Access"
        : premium
          ? "Local Premium Pass"
          : "Local Official Pass",
    description: "Generated by the local-model integration test server.",
    mood,
    requiredFields: [
      "name",
      "memberId",
      ...(identity ? ["photo"] : []),
      ...(creative ? ["decorativeArt"] : []),
      "qrCode",
    ],
    designTokens: {
      primaryColor,
      secondaryColor,
      textColor: premium || creative ? "#ffffff" : "#111827",
      orientation: identity ? "portrait" : "landscape",
      backgroundMode: creative ? "pattern" : premium ? "gradient" : "solid",
      colorUsage: premium || creative ? "dominant" : "balanced",
      brandAssetSource: "logo",
      brandAssetTreatment: creative ? "side-emblem" : "logo-watermark",
      brandAssetIntensity: creative ? "bold" : "subtle",
      usesLogo: true,
      usesQr: true,
      usesPhoto: identity,
      usesDecorativeArt: creative,
    },
  };
}

const server = http.createServer(async (request, response) => {
  if (request.url === "/health") {
    response.writeHead(200, { "Content-Type": "application/json" });
    response.end(JSON.stringify({ status: "ok", model: "mock-card-designer" }));
    return;
  }

  if (request.method !== "POST" || request.url !== "/v1/chat/completions") {
    response.writeHead(404, { "Content-Type": "application/json" });
    response.end(JSON.stringify({ error: "Not found" }));
    return;
  }

  let body = "";
  for await (const chunk of request) body += chunk;
  const payload = JSON.parse(body);
  const prompt =
    [...(payload.messages ?? [])]
      .reverse()
      .find((message) => message.role === "user")?.content ?? "";
  const concept = conceptFromPrompt(prompt);

  response.writeHead(200, { "Content-Type": "application/json" });
  response.end(
    JSON.stringify({
      id: "mock-completion",
      object: "chat.completion",
      model: payload.model ?? "mock-card-designer",
      choices: [
        {
          index: 0,
          finish_reason: "stop",
          message: {
            role: "assistant",
            content: JSON.stringify(concept),
          },
        },
      ],
      usage: {
        prompt_tokens: Math.ceil(prompt.length / 4),
        completion_tokens: Math.ceil(JSON.stringify(concept).length / 4),
        total_tokens: Math.ceil((prompt.length + JSON.stringify(concept).length) / 4),
      },
    }),
  );
});

server.listen(port, "127.0.0.1", () => {
  console.log(`Mock local LLM listening at http://127.0.0.1:${port}/v1`);
});
