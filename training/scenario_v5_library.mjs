const moodCurricula = {
  official,
  minimal,
  premium,
  playful,
  creative,
  cyberpunk,
  academic,
  luxury,
  event,
  identity,
};

const allowedOrientations = new Set(["landscape", "portrait"]);
const allowedBackgrounds = new Set(["solid", "gradient", "image", "image-overlay", "pattern"]);
const allowedColorUsage = new Set(["subtle", "balanced", "dominant", "full"]);
const allowedAssetSources = new Set(["none", "logo", "hero", "background"]);
const allowedTreatments = new Set([
  "standard", "logo-watermark", "background-emblem", "hero-backdrop", "side-emblem",
]);
const allowedIntensities = new Set(["subtle", "medium", "bold"]);
const allowedScenarioFields = new Set([
  "photo", "email", "phone", "tier", "expiryDate", "dateJoined",
  "studentId", "course", "loyaltyPoints", "decorativeArt",
]);
const lockedFields = new Set(["name", "memberId", "qrCode"]);

function finalizeScenario(mood, task, spec) {
  const prefix = task === "generation" ? "g5" : "r5";
  const scenario = {
    key: `${prefix}-${mood}-${spec.key}`,
    task,
    mood,
    prompt: spec.prompt,
    orientation: spec.orientation,
    backgroundMode: spec.backgroundMode,
    colorUsage: spec.colorUsage,
    preferredAsset: spec.preferredAsset,
    treatment: spec.treatment,
    intensity: spec.intensity,
    usesPhoto: spec.usesPhoto,
    usesDecorativeArt: spec.usesDecorativeArt,
    fields: spec.fields,
    removeFields: spec.removeFields,
    current: spec.current ?? null,
    label: spec.label,
    summary: spec.summary,
  };

  const fail = (message) => {
    throw new Error(`Scenario V5 scenario ${scenario.key}: ${message}`);
  };

  if (!spec.prompt || spec.prompt.length < 80 || spec.prompt.length > 320) {
    fail(`prompt length ${spec.prompt?.length ?? 0} outside 80..320`);
  }
  if (!allowedOrientations.has(spec.orientation)) fail(`bad orientation ${spec.orientation}`);
  if (!allowedBackgrounds.has(spec.backgroundMode)) fail(`bad backgroundMode ${spec.backgroundMode}`);
  if (!allowedColorUsage.has(spec.colorUsage)) fail(`bad colorUsage ${spec.colorUsage}`);
  if (!allowedAssetSources.has(spec.preferredAsset)) fail(`bad preferredAsset ${spec.preferredAsset}`);
  if (!allowedTreatments.has(spec.treatment)) fail(`bad treatment ${spec.treatment}`);
  if (!allowedIntensities.has(spec.intensity)) fail(`bad intensity ${spec.intensity}`);
  if (typeof spec.usesPhoto !== "boolean" || typeof spec.usesDecorativeArt !== "boolean") {
    fail("usesPhoto/usesDecorativeArt must be booleans");
  }
  if (!Array.isArray(spec.fields) || !Array.isArray(spec.removeFields)) {
    fail("fields and removeFields must be arrays");
  }
  for (const field of [...spec.fields, ...spec.removeFields]) {
    if (field === "photo" && spec.removeFields.includes("photo")) continue;
    if (!allowedScenarioFields.has(field)) fail(`field ${field} not allowed`);
    if (lockedFields.has(field)) fail(`locked field ${field} may not be listed`);
  }
  if (spec.fields.some((field) => spec.removeFields.includes(field))) {
    fail("fields and removeFields overlap");
  }
  if (spec.usesPhoto && spec.removeFields.includes("photo")) fail("usesPhoto but photo removed");
  if (spec.usesDecorativeArt && spec.removeFields.includes("decorativeArt")) {
    fail("usesDecorativeArt but decorativeArt removed");
  }
  if (!spec.usesPhoto && spec.fields.includes("photo")) fail("photo listed without usesPhoto");
  if (!spec.usesDecorativeArt && spec.fields.includes("decorativeArt")) {
    fail("decorativeArt listed without usesDecorativeArt");
  }
  if (spec.fields.length > 5) fail("more than 5 extra fields");
  if (!spec.label || spec.label.length > 40) fail("label missing or longer than 40 chars");
  if (!spec.summary || spec.summary.length > 56) fail("summary missing or longer than 56 chars");
  if (task === "generation" && spec.current) fail("generation scenarios must not define current");
  if (task === "refinement" && spec.removeFields.length > 0) {
    for (const field of spec.removeFields) {
      if (field === "photo" || allowedScenarioFields.has(field)) continue;
      fail(`removeFields entry ${field} not recognized`);
    }
  }

  return Object.freeze(scenario);
}

export const scenarioV5Scenarios = Object.entries(moodCurricula).flatMap(([mood, curriculum]) =>
  ["generation", "refinement"].flatMap((task) => {
    const specs = curriculum[task];
    if (specs.length !== 15) {
      throw new Error(`Scenario V5 library: ${mood}/${task} has ${specs.length} scenarios, expected 15`);
    }
    return specs.map((spec) => finalizeScenario(mood, task, spec));
  }),
);

const keys = new Set(scenarioV5Scenarios.map((scenario) => scenario.key));
const prompts = new Set(
  scenarioV5Scenarios.map((scenario) => scenario.prompt.toLowerCase().replace(/\s+/g, " ").trim()),
);
if (scenarioV5Scenarios.length !== 300) {
  throw new Error(`Scenario V5 library must contain exactly 300 scenarios, found ${scenarioV5Scenarios.length}`);
}
if (keys.size !== 300) throw new Error("Scenario V5 library contains duplicate scenario keys");
if (prompts.size !== 300) throw new Error("Scenario V5 library contains duplicate scenario prompts");
